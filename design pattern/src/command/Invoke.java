package command;
//调用者/发起者
public class Invoke {
	private Command command;//也可以通过容器List<Command> 容纳很多命令对象，进行批处理，根据数据库底层的事物管理就是类似的结构

	public Invoke(Command command) {
		super();
		this.command = command;
	}
	
	//业务方法，用于调用命令的方法
	public void call(){
		System.out.println("加点料");
		this.command.execute();
	}
}
